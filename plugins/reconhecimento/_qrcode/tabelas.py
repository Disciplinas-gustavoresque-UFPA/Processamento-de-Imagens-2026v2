def _criar_blocos_ec() -> dict[int, dict[str, tuple[int, list[tuple[int, int]]]]]:
    blocos: dict[int, dict[str, tuple[int, list[tuple[int, int]]]]] = {}
    for versao in range(1, 41):
        blocos[versao] = {}
        for nivel in ("L", "M", "Q", "H"):
            ec_por_bloco = ECC_CODEWORDS_PER_BLOCK[nivel][versao]
            quantidade_blocos = EC_BLOCK_COUNT[nivel][versao]
            dados_totais = TOTAL_CODEWORDS[versao] - ec_por_bloco * quantidade_blocos
            dados_base, blocos_maiores = divmod(dados_totais, quantidade_blocos)

            grupos = []
            blocos_menores = quantidade_blocos - blocos_maiores
            if blocos_menores:
                grupos.append((blocos_menores, dados_base))
            if blocos_maiores:
                grupos.append((blocos_maiores, dados_base + 1))

            blocos[versao][nivel] = (ec_por_bloco, grupos)
    return blocos


EC_BLOCKS = _criar_blocos_ec()
